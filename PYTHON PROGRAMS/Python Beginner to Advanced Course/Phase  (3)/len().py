# Example 1: Length of a simple string
text = "Hello, World!"
print(len(text))  # Output: 13

# Example 2: Length of an empty string
empty = ""
print(len(empty))  # Output: 0

# Example 3: Length of a string with spaces
sentence = "Python is fun"
print(len(sentence))  # Output: 13

# Example 4: Length of a string with special characters
special = "Ayaan@123!"
print(len(special))  # Output: 10