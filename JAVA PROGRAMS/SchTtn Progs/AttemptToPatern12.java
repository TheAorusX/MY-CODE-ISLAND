    class AttemptToPatern12
{
    public static void main(String[]ARGS)
    {
        int i,j,t=1;
        for(i=5;i>=1;i--)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print(t+" ");
                t++;
            }
            System.out.println();
        }
    }
}